class VigenereCipheringMachine {
  constructor(direct = true) {
    this.direct = direct;
  }

  keyToCode(str, key) {
    const strWithoutSpaces = str.split('').filter((v) => table.A.includes(v)).join('');
    let code = key;
    while(code.length < strWithoutSpaces.length) {
      code += code;
    }
    return code.slice(0, strWithoutSpaces.length);
  }

  encrypt(strCase, key) {
    if(!strCase || !key) throw new Error('Incorrect arguments!');


    const str = strCase.toUpperCase();
    const code = this.keyToCode(str, key.toUpperCase());
    let crypt = '';

    for(let i = 0, j = 0; j < str.length;) {
      if(!table.A.includes(str[j])) {
        crypt += str[j];
        j += 1;
        continue;
      }
      const x = table.A.indexOf(str[j]); 
      crypt += table[code[i]][x];
      i += 1;
      j += 1;
    }
    return this.direct ? crypt : crypt.split('').reverse().join('');
  }

  decrypt(str, key) {
    if(!str || !key) throw new Error('Incorrect arguments!');

    const code = this.keyToCode(str, key.toUpperCase());
    let decryptStr = '';

    for(let i = 0, j = 0; j < str.length;) {
      if(!table.A.includes(str[j])) {
        decryptStr += str[j];
        j += 1;
        continue;
      }
      const x = table[code[i]].indexOf(str[j]);
      decryptStr += table.A[x];
      i += 1;
      j += 1;
    }
    return this.direct ? decryptStr : decryptStr.split('').reverse().join('');
  }
}

